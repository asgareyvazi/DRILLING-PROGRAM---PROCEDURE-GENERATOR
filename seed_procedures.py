def _add(db, cat_id, key, name, now, description,
         steps=None, checklist=None) -> int:
    """Helper: Add one procedure if not exists"""
    # Check if exists
    existing = db.conn.execute(
        "SELECT id FROM procedures WHERE proc_key=?",
        (key,)).fetchone()
    if existing:
        return 0

    cur = db.conn.execute(
        "INSERT INTO procedures "
        "(proc_key, name, category_id, description, has_checklist, "
        "is_builtin, is_active, created_date, modified_date) "
        "VALUES (?, ?, ?, ?, 1, 1, 1, ?, ?)",
        (key, name, cat_id, description, now, now))
    proc_id = cur.lastrowid

    if steps:
        for idx, s in enumerate(steps):
            indent, is_hdr, text = s
            db.conn.execute(
                "INSERT INTO procedure_steps "
                "(procedure_id, step_number, indent_level, text, "
                "is_header, is_note, is_warning) "
                "VALUES (?, ?, ?, ?, ?, 0, 0)",
                (proc_id, idx + 1, indent, text, int(is_hdr)))

    if checklist:
        for idx, (text, category) in enumerate(checklist):
            db.conn.execute(
                "INSERT INTO checklist_items "
                "(procedure_id, item_number, text, category) "
                "VALUES (?, ?, ?, ?)",
                (proc_id, idx + 1, text, category))

    db.conn.commit()
    return 1


