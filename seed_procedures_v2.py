    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    count = 0

    # Get category IDs
    cats = {}
    for c in db.get_all_categories():
        cats[c.name] = c.id

    # ================================================================
    # HELPER
    # ================================================================

    def add(cat_name, key, name, desc, steps=None,
            checklist=None, inputs=None):
        nonlocal count
        cat_id = cats.get(cat_name, 1)

        # Check if exists → update, else insert
        existing = db.conn.execute(
            "SELECT id FROM procedures WHERE proc_key=?",
            (key,)).fetchone()

        if existing:
            pid = existing['id']
            db.update_procedure(
                pid, name=name, category_id=cat_id,
                description=desc)
            db.conn.execute(
                "DELETE FROM procedure_steps WHERE procedure_id=?",
                (pid,))
            db.conn.execute(
                "DELETE FROM checklist_items WHERE procedure_id=?",
                (pid,))
            db.conn.execute(
                "DELETE FROM procedure_inputs WHERE procedure_id=?",
                (pid,))
        else:
            cur = db.conn.execute(
                "INSERT INTO procedures "
                "(proc_key, name, category_id, description, "
                "has_checklist, is_builtin, is_active, "
                "created_date, modified_date) "
                "VALUES (?, ?, ?, ?, 1, 1, 1, ?, ?)",
                (key, name, cat_id, desc, now, now))
            pid = cur.lastrowid

        if steps:
            for idx, s in enumerate(steps):
                indent, is_hdr, text = s[0], s[1], s[2]
                is_note = s[3] if len(s) > 3 else False
                is_warn = s[4] if len(s) > 4 else False
                db.conn.execute(
                    "INSERT INTO procedure_steps "
                    "(procedure_id, step_number, indent_level, "
                    "text, is_header, is_note, is_warning) "
                    "VALUES (?, ?, ?, ?, ?, ?, ?)",
                    (pid, idx+1, indent, text,
                     int(is_hdr), int(is_note), int(is_warn)))

        if checklist:
            for idx, (text, category) in enumerate(checklist):
                db.conn.execute(
                    "INSERT INTO checklist_items "
                    "(procedure_id, item_number, text, category) "
                    "VALUES (?, ?, ?, ?)",
                    (pid, idx+1, text, category))

        if inputs:
            for idx, inp in enumerate(inputs):
                db.conn.execute(
                    "INSERT INTO procedure_inputs "
                    "(procedure_id, input_key, input_label, "
                    "input_type, input_options, input_default, "
                    "input_unit, input_order, is_required) "
                    "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)",
                    (pid, inp.get('key', ''),
                     inp.get('label', ''),
                     inp.get('type', 'text'),
                     inp.get('options', ''),
                     inp.get('default', ''),
                     inp.get('unit', ''),
                     idx, int(inp.get('required', False))))

        db.conn.commit()
        count += 1
        return pid

